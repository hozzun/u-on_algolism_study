# 키 큰사람부터 차례대로 넣으면 된다고 해서 범위를 뒤에서부터 하는거로 바꿔줌 그랬더니 맞았다! 
# 4를 0번 인덱스에 ( 4 ) , 3을 1번 인덱스 ( 4 3 ) , 2를 1번 인덱스 ( 4 2 3 ) , 1을 2번 인덱스 ( 4 2 1 3 ) 
# 이렇게 넣어줌!!!   
N = int(input())
height = list(map(int, input().split()))
lst = []
for i in range(N - 1, -1, -1):
    lst.insert(height[i] , i + 1)

print(' '.join(map(str, lst)))
