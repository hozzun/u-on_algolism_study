#이 코드는 2번 인덱스에 1 ( 1 ) , 1번 인덱스에 2 ( 1 2 ) , 1 번 인덱스에 3 ( 1 3 2 ) , 0번 인덱스에 4 ( 4 1 3 2)
# 순서가 이상하게 들어감 ㅜ..  

N = int(input())
height = list(map(int, input().split()))
lst = []
for i in range(N):
    lst.insert(height[i], i + 1)

print(' '.join(map(str, lst)))

